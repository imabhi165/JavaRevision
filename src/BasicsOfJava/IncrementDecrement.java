package BasicsOfJava;

public class IncrementDecrement {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x--; //x-- ka matlab x = x - 1;
        System.out.println(x);
        x++;
        System.out.println(x);
        //this is single line comment
        ++x;
        System.out.println(x);
        --x;
        System.out.println(x);


    }
}
